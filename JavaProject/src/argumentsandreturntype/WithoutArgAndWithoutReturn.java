//Without Argument and without Return type


package argumentsandreturntype;

public class WithoutArgAndWithoutReturn {
	
	public void m1()
	{
		System.out.println("Without Argument and without Return type");
	}
	public static void m2()
	{
		System.out.println("Without Argument and without Return type");
	}
	public static void main(String[] args) {
		WithoutArgAndWithoutReturn	aa = new WithoutArgAndWithoutReturn();
		aa.m1();
		m2();
		
	}
}


//Void is return type which returns nothing (Null )