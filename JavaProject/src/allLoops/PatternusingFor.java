package allLoops;

public class PatternusingFor {
	public static void main(String[] args) {
		for(int i=1;i<=5;i++) {//in this loop i valu when increse 
			for(int j=1;j<=5;j++) {//this is nested loop
				System.out.print(" * ");//use horizontaly use \t
			}
			System.out.println("");
		}
	}

}
